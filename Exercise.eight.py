# یک متغیر ایجاد کنید و سن خود را در آن بنویسد. سالی که در
# آن 100 ساله خواهد شد را محاسبه کنید


# تعریف متغیر و ذخیره سن کنونی
age = int(input("Enter age:\t"))

# محاسبه سالی که 100 ساله خواهید شد
current_year = 1403   #سال کنونی را وارد کنید
years_to_100 = 100 - age
year_turn_100 = current_year + years_to_100

print(f"شما در سال {year_turn_100} صد ساله خواهید شد.")
