# برنامه ای ایجاد کنید که دما را از سانتیگراد به فارنهایت تبدیل
# کند. دما را بر حسب سانتیگراد در یک متغیر ذخیره کنید، تبدیل را
# انجام دهید و نتیجه را چاپ کنید.

# رای تبدیل دما از سانتیگراد به فارنهایت=Fahrenheit=32+(9/5*Celsius)
celsius_c = float(input("\t :دمای مورد نظر بر حسب سانتیگراد را وارد کن"))
fahrenheit = (celsius_c * 9 / 5) + 32
print(F" دمای معادل درفارنهایت {fahrenheit}")
